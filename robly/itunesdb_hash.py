"""iTunesDB hash58 computation for iPod Touch 1G iOS 1.1.2.

Pure-Python port of libgpod's itdb_hash58.c (Christophe Fergeau, based on wtbw's
proof of concept). Independently verified against 3 captured iTunesDBs.

iTunes 11.2 protects this with FairPlay-style anti-RE in itunes.dll, but the
underlying algorithm is straightforward once known: HMAC-SHA1 with a key
derived from the iPod's UDID via AES S-box substitution + SHA1 of a fixed
constant.

Without a valid hash58 in the mhbd header, iPod Touch 1G shows an empty Music
library after sync.
"""
import hashlib
import struct


# Which hash algorithm each iPod ProductType needs in its iTunesDB.
# `none` = no checksum (older models, just write the DB as-is)
# `hash58` = libgpod HMAC-SHA1 with UDID-derived key (implemented here)
# `hash72` = libgpod AES-CBC with per-device HashInfo (not implemented yet)
# `hashAB` = newer scheme used by iPod nano 6G and iOS 4+ (not implemented yet)
DEVICE_HASH_ALGO = {
    # iPod Touch generations
    "iPod1,1": "hash58",  # Touch 1G — verified working
    "iPod2,1": "hash72",  # Touch 2G — needs HashInfo
    "iPod3,1": "hash72",  # Touch 3G
    "iPod4,1": "hash72",  # Touch 4G
    # iPod Classic / Video — also hash58
    "iPod5,1": "hash58",  # Classic 6G (160GB)
    "iPod6,1": "hash58",  # Classic 7G
    # iPod Nano
    "iPod3,1 [Nano 3G]": "hash58",
    "iPod4,1 [Nano 4G]": "hash58",
    "iPod6,1 [Nano 6G]": "hashAB",
    # Older — no hash
    "iPod1,2": "none",  # Mini, etc.
}


def algo_for_product_type(product_type: str) -> str:
    """Best-effort lookup. Defaults to `hash58` (works for most pre-2008 iPods)."""
    return DEVICE_HASH_ALGO.get(product_type, "hash58")

