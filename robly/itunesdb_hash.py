# mhbd header field offsets
OFF_DB_ID    = 0x18  # 8 bytes
OFF_HASHING  = 0x30  # 2 bytes (set to 1 = ITDB_CHECKSUM_HASH58 LE)
OFF_UNK_0X32 = 0x32  # 20 bytes
OFF_HASH58   = 0x58  # 20 bytes

ITDB_CHECKSUM_HASH58 = 1


def _gcd(a: int, b: int) -> int:
    while True:
        a = a % b
        if a == 0:
            return b
        b = b % a
        if b == 0:
            return a


def _lcm(a: int, b: int) -> int:
    if a == 0 or b == 0:
        return 1
    return (a * b) // _gcd(a, b)


def _derive_key(firewire_id: bytes) -> bytes:
    """Derive 64-byte HMAC key from 8-byte firewire_id."""
    y = bytearray(16)
    for i in range(4):
        a, b = firewire_id[i*2], firewire_id[i*2+1]
        cur_lcm = _lcm(a, b)
        hi = (cur_lcm >> 8) & 0xff
        lo = cur_lcm & 0xff
        y[i*4 + 0] = _TABLE1[hi]
        y[i*4 + 1] = _TABLE2[hi]
        y[i*4 + 2] = _TABLE1[lo]
        y[i*4 + 3] = _TABLE2[lo]
    seed = hashlib.sha1(_FIXED + bytes(y)).digest()  # 20 bytes
    return seed + b"\x00" * (64 - len(seed))


def write_hash58(itdb_data: bytes, udid: str) -> bytes:
    """Return iTunesDB bytes with a valid hash58 written at offset 0x58.

    `udid` is the 40-character hex UDID string (e.g. as found in the pair record
    or returned by lockdown's `UniqueDeviceID`). Only the first 8 raw bytes
    (= first 16 hex chars) are used.
    """
    if len(itdb_data) < OFF_HASH58 + 20:
        raise ValueError("iTunesDB too small to hold hash58")

    if isinstance(udid, str):
        firewire_id = bytes.fromhex(udid)[:8]
    else:
        firewire_id = bytes(udid)[:8]
    if len(firewire_id) != 8:
        raise ValueError("firewire_id must be 8 bytes (16 hex chars of UDID)")

    data = bytearray(itdb_data)

    # Backup fields that must be restored after hashing
    backup_db_id = bytes(data[OFF_DB_ID:OFF_DB_ID + 8])
    backup_unk32 = bytes(data[OFF_UNK_0X32:OFF_UNK_0X32 + 20])

    # Zero the fields hashed-as-zero by the algorithm
    for i in range(OFF_DB_ID, OFF_DB_ID + 8):
        data[i] = 0
    for i in range(OFF_UNK_0X32, OFF_UNK_0X32 + 20):
        data[i] = 0
    for i in range(OFF_HASH58, OFF_HASH58 + 20):
        data[i] = 0

    # Set hashing_scheme = HASH58 (1)
    struct.pack_into("<H", data, OFF_HASHING, ITDB_CHECKSUM_HASH58)

    # HMAC-SHA1 with derived key
    key = _derive_key(firewire_id)
    ipad = bytes(b ^ 0x36 for b in key)
    opad = bytes(b ^ 0x5c for b in key)
    inner = hashlib.sha1(ipad + bytes(data)).digest()
    hash58 = hashlib.sha1(opad + inner).digest()

    # Restore backed-up fields and write the hash
    data[OFF_DB_ID:OFF_DB_ID + 8] = backup_db_id
    data[OFF_UNK_0X32:OFF_UNK_0X32 + 20] = backup_unk32
    data[OFF_HASH58:OFF_HASH58 + 20] = hash58

    return bytes(data)
